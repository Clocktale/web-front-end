@Injectable()
export class AnimeRepositoryMock extends AnimeRepository {
  list(query?: AnimeListQuery): Observable<AnimePaginatedResult> {
    const page = query?.page ?? 1;
    const pageSize = query?.pageSize ?? 24;
    const total = MOCK_ANIMES.length;
    const from = (page - 1) * pageSize;
    const items = MOCK_ANIMES.slice(from, from + pageSize);

    return of({
      items,
      total,
      currentPage: page,
      lastPage: Math.max(1, Math.ceil(total / pageSize)),
      perPage: pageSize,
      from: items.length > 0 ? from + 1 : null,
      to: items.length > 0 ? from + items.length : null,
    }).pipe(delay(400));
  }
}
